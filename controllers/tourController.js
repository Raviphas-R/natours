// /tours-within//233/center/34.013487, -118.268841/unit/mi

// getTourwithin distance
exports.getToursWithin = catchAsync(async (req, res, next) => {
  const { distance, latlng, unit } = req.params;
  const [lat, lng] = latlng.split(',');

  // Convert distance in {unit} TO distance in Radian.
  const radius = unit === 'mi' ? distance / 3963.2 : distance / 6378.1;

  if (!lat || !lng) {
    next(
      new AppError(
        'Please provide latitide and longitude in format lat, lng.',
        404
      )
    );
  }

  const tours = await Tour.find({
    // geoWithin expect radius in RADIAN unit
    startLocation: { $geoWithin: { $centerSphere: [[lng, lat], radius] } },
  });

  res.status(200).json({
    status: 'success',
    results: tours.length,
    data: {
      data: tours,
    },
  });
});

exports.getDistances = catchAsync(async (req, res, next) => {
  const { latlng, unit } = req.params;
  const [lat, lng] = latlng.split(',');

  const multiplier = unit === 'mi' ? 0.000621371 : 0.001;

  if (!lat || !lng) {
    next(
      new AppError(
        'Please provide latitide and longitude in format lat, lng.',
        404
      )
    );
  }

  const distances = await Tour.aggregate([
    {
      // $geoNear must to be the first one of pipeline
      $geoNear: {
        near: {
          type: 'Point',
          coordinates: [+lng, +lat],
        },
        // key: startLocation, --> This no need to define because there are only one index tourSchema.index({ startLocation: '2dsphere' });
        distanceField: 'distance',
        distanceMultiplier: multiplier,
      },
    },
    {
      $project: {
        distance: 1,
        name: 1,
      },
    },
  ]);

  res.status(200).json({
    status: 'success',
    data: {
      data: distances,
    },
  });
});
